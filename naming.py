# naming conventions/cases
# 1. snake_case - used for variables and functions e.g snake_case_variable, snake_case_function()
# 2. camelCase - used for variables and functions e.g camelCaseVariable, camelCaseFunction()
# 3. PascalCase - used for classes in languages like Python e.g PascalCaseClass
# 4. kebab-case - used for naming ids and classes in languages like CSS/HTML/JavaScript 
# 5. SCREAMING_SNAKE_CASE - used for constants in languages like Python e.g SCREAMING_SNAKE_CASE_CONSTANT

# Rules of naming variables
# 1. Variable names must start with a letter (a-z, A-Z) or an underscore (_).
# 2. Variable names cannot start with a number (0-9).
# 3. Variable names can only contain letters, numbers, and underscores.
# 4. Variable names are case-sensitive (e.g., myVariable and myvariable are different variables).
# 5. Variable names cannot be the same as reserved keywords in Python (e.g., if, else, while, for, def, class,etc.).
# PEP 8 - Style Guide for Python Code - https://peps.python.org/pep-0008/#naming-conventions