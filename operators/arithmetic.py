# Arithmetic operations - they are used to perform mathematical operations like addition, subtraction, multiplication, etc.
# Addition
a = 10
b = 5
print(a + b)  

# Subtraction
print(a - b)

# Multiplication
print(a * b)

# Division
print(a / b)

# Floor Division - it gives the quotient of the division without the decimal part
a = 12
b = 5
print(a // b)

# Modulus - it gives the remainder of the division
# print(a % b)
print(9 % 2)

# Exponentiation - it raises a number to the power of another number
# print(a ** b)
print(2 ** 3)