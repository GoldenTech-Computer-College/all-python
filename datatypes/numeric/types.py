# Integers - Whole numbers without a decimal point. They can be positive, negative, or zero.
# represented by the int() class
a = 10
b = -5
c = 0
print(type(a))
print(type(b))
print(type(c))

# Floats - Numbers that have a decimal point. They can also be positive, negative, or zero.
# represented by the float() class
pi = 3.142
e = 2.718
negative_float = -0.5
c = 0.0

print(type(pi))
print(type(e))
print(type(negative_float))
print(type(c))

# Complex Numbers - Numbers that have a real part and an imaginary part. They are represented by the complex() class.
z1 = 2 + 3j
z2 = 1 - 4j
print(type(z1))
print(type(z2))